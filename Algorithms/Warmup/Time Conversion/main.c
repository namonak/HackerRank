char* timeConversion(char* s) {
    char *ret = NULL;
    char hour[3] = {0, };
    char ampm[3] = {0, };
    int len = strlen(s);
    int tmp = 0;
    char temp[10];

    ret = (char*)malloc(len + 1);

    snprintf(hour, 3, "%s", s);
    tmp = atoi(hour);

    ampm[0] = s[len - 2];
    ampm[1] = s[len - 1];
    ampm[2] = '\0';

    if (strcmp(ampm, "AM") == 0) {
        if (tmp == 12) {
            strncpy(temp, s + 2, len - 4);
            sprintf(ret, "%s%s", "00", temp);
        } else {
            strncpy(ret, s, len - 2);
        }
    } else {
        if (tmp == 12) {
            strncpy(ret, s, len - 2);
        } else {
            strncpy(temp, s + 2, len - 4);
            sprintf(ret, "%d%s", tmp + 12, temp);
        }
    }

    return ret;
}

